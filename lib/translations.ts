function readValue(tree: TranslationValue | undefined, key: string): unknown {
  return key.split('.').reduce<unknown>((current, part) => {
    if (current && typeof current === 'object' && part in current) {
      return (current as Record<string, unknown>)[part]
    }

    return undefined
  }, tree)
}

export function translateText(language: Language, key: string, fallback = '') {
  const value = readValue(translations[language], key)
  if (typeof value === 'string') return value

  const defaultValue = readValue(translations[defaultLanguage], key)
  if (typeof defaultValue === 'string') return defaultValue

  return fallback
}

export function translateStringArray(language: Language, key: string, fallback: readonly string[] = []) {
  const value = readValue(translations[language], key)
  if (Array.isArray(value) && value.every((item) => typeof item === 'string')) {
    return value as string[]
  }

  const defaultValue = readValue(translations[defaultLanguage], key)
  if (Array.isArray(defaultValue) && defaultValue.every((item) => typeof item === 'string')) {
    return defaultValue as string[]
  }

  return [...fallback]
}
