export function translateStaticText(language: Language, text: string) {
  if (language === 'ja') return text

  const normalized = text.replace(/\s+/g, ' ').trim()
  if (!normalized) return text

  const questionsMatch = normalized.match(/^Questions:\s*(\d+)$/)
  if (questionsMatch) {
    if (language === 'zh') return `问题：${questionsMatch[1]}`
    if (language === 'es') return `Preguntas: ${questionsMatch[1]}`
    return `Questions: ${questionsMatch[1]}`
  }

  const translated =
    staticTextTranslations[normalized]?.[language] ??
    supplementalStaticTextTranslations[normalized]?.[language]
  if (!translated) return text

  const leading = text.match(/^\s*/)?.[0] ?? ''
  const trailing = text.match(/\s*$/)?.[0] ?? ''
  return `${leading}${translated}${trailing}`
}
